package Lab6.generellSim;
/**
 * The class that stops and starts the simulator.
 * 
 * @author Ludvig Lidén, Botan Guzel, Sergij Wennström
 *
 */
public abstract class Sim {
	
	/**
	 * Abstract class that starts the simulator.
	 */
	public abstract void startaSimulator();
	
	/**
	 * Abstract class that ends the simulator.
	 */
	public abstract void avslutaSimulator();
}